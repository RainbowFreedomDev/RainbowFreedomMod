package me.StevenLawson.TotalFreedomMod.Commands;

import me.StevenLawson.TotalFreedomMod.TFM_AdminList;
import me.StevenLawson.TotalFreedomMod.TFM_BanManager;
import me.StevenLawson.TotalFreedomMod.TFM_CommandBlocker;
import me.StevenLawson.TotalFreedomMod.TFM_Log;
import me.StevenLawson.TotalFreedomMod.TFM_PermbanList;
import me.StevenLawson.TotalFreedomMod.TFM_PlayerList;
import me.StevenLawson.TotalFreedomMod.TFM_Util;
import me.StevenLawson.TotalFreedomMod.TotalFreedomMod;
import org.bukkit.ChatColor;
import org.bukkit.command.Command;
import org.bukkit.command.CommandSender;
import org.bukkit.entity.Player;

@CommandPermissions(level = AdminLevel.ALL, source = SourceType.BOTH)
@CommandParameters(description = "Shows information about RainbowFreedomMod or reloads it", usage = "/<command> [reload]")
public class Command_rfm extends TFM_Command
{
    @Override
    public boolean run(CommandSender sender, Player sender_p, Command cmd, String commandLabel, String[] args, boolean senderIsConsole)
    {
        if (args.length == 1)
        {
            if (!args[0].equals("reload"))
            {
                return false;
            }

            if (!TFM_AdminList.isSeniorAdmin(sender))
            {
                playerMsg(ChatColor.RED + "You don't have permission to reload the plugin!");
                return true;
            }

            TFM_AdminList.load();
            TFM_PermbanList.load();
            TFM_PlayerList.load();
            TFM_BanManager.load();
            TFM_CommandBlocker.load();

            final String message = String.format("%s v%s.%s reloaded.",
                    TotalFreedomMod.pluginName,
                    TotalFreedomMod.pluginVersion,
                    TotalFreedomMod.buildNumber);

            playerMsg(message);
            TFM_Log.info(message);
            return true;
        }

        playerMsg("You are running the RainbowFreedomMod for RainbowFreedom!", ChatColor.GOLD);
        playerMsg("Created by: " + ChatColor.RED + "FoxIshDaBest", ChatColor.GOLD);
        StringBuilder developers = new StringBuilder();
        for (String dev : TFM_Util.RFM_DEVELOPERS)
        {
            developers.append(", " + dev);
        }
        playerMsg("Visit " + ChatColor.GOLD + "http://rainbowfreedom.boards.net/" + ChatColor.YELLOW + " to visit our forums and get support!", ChatColor.YELLOW);

        return true;
    }
}
